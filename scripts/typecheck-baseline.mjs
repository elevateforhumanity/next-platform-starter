#!/usr/bin/env node
/**
 * Baseline-aware TypeScript gate.
 *
 * The repository currently has known TypeScript debt. This script preserves
 * signal by comparing the current `tsc --noEmit` error set against a committed
 * baseline and failing only when new error lines appear.
 */

import { spawnSync } from 'node:child_process';
import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import process from 'node:process';

const DEFAULT_BASELINE = 'docs/typecheck-baseline.txt';
const args = process.argv.slice(2);
const writeBaseline = args.includes('--write-baseline');
const baselineFlagIndex = args.indexOf('--baseline');
const baselinePath =
  baselineFlagIndex >= 0 && args[baselineFlagIndex + 1]
    ? args[baselineFlagIndex + 1]
    : DEFAULT_BASELINE;

function stripAnsi(value) {
  return value.replace(/\u001b\[[0-9;]*m/g, '');
}

function normalizeErrorLine(line) {
  return stripAnsi(line)
    .replaceAll(process.cwd() + path.sep, '')
    .replaceAll('\\', '/')
    .trim();
}

function collectTypecheckErrors() {
  const result = spawnSync('pnpm', ['exec', 'tsc', '--noEmit', '--pretty', 'false'], {
    cwd: process.cwd(),
    encoding: 'utf8',
    env: {
      ...process.env,
      NODE_OPTIONS: process.env.NODE_OPTIONS || '--max-old-space-size=8192',
    },
    maxBuffer: 1024 * 1024 * 20,
  });

  const output = `${result.stdout ?? ''}\n${result.stderr ?? ''}`;
  return output
    .split('\n')
    .map(normalizeErrorLine)
    .filter((line) => /error TS\d+:/.test(line))
    .sort();
}

function readBaseline(filePath) {
  if (!existsSync(filePath)) return null;
  return readFileSync(filePath, 'utf8')
    .split('\n')
    .map((line) => line.trim())
    .filter(Boolean)
    .filter((line) => !line.startsWith('#'));
}

function unique(lines) {
  return [...new Set(lines)];
}

const currentErrors = unique(collectTypecheckErrors());
const absoluteBaseline = path.resolve(process.cwd(), baselinePath);

if (writeBaseline) {
  mkdirSync(path.dirname(absoluteBaseline), { recursive: true });
  const header = [
    '# TypeScript baseline errors',
    '# Generated by: pnpm typecheck:baseline',
    '# This file is debt telemetry. Do not add new errors; use pnpm typecheck:changed.',
    `# Error count: ${currentErrors.length}`,
    '',
  ];
  writeFileSync(absoluteBaseline, `${header.join('\n')}${currentErrors.join('\n')}\n`);
  console.log(`Wrote ${currentErrors.length} TypeScript baseline errors to ${baselinePath}`);
  process.exit(0);
}

const baseline = readBaseline(absoluteBaseline);
if (!baseline) {
  console.error(`Missing TypeScript baseline: ${baselinePath}`);
  console.error('Run: pnpm typecheck:baseline');
  process.exit(1);
}

const baselineSet = new Set(baseline);
const currentSet = new Set(currentErrors);
const newErrors = currentErrors.filter((line) => !baselineSet.has(line));
const resolvedErrors = baseline.filter((line) => !currentSet.has(line));

console.log(`TypeScript baseline errors: ${baseline.length}`);
console.log(`Current TypeScript errors: ${currentErrors.length}`);
console.log(`Resolved baseline errors: ${resolvedErrors.length}`);
console.log(`New TypeScript errors: ${newErrors.length}`);

if (newErrors.length > 0) {
  console.error('\nNew TypeScript errors:');
  for (const line of newErrors.slice(0, 80)) {
    console.error(line);
  }
  if (newErrors.length > 80) {
    console.error(`...and ${newErrors.length - 80} more`);
  }
  process.exit(1);
}

if (currentErrors.length > 0) {
  console.log('\nTypeScript debt remains, but no new errors were introduced.');
}

process.exit(0);

