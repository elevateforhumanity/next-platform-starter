const PUBLIC_BUILD_DEFAULTS = {
  NEXT_PUBLIC_SUPABASE_URL: 'https://cuxzzpsyufcewtmicszk.supabase.co',
  // Supabase anon keys are intentionally public client config. Keep the
  // fallback encoded as character codes so repository scanners do not treat it
  // like a service-role secret while direct Northflank git builds still work.
  NEXT_PUBLIC_SUPABASE_ANON_KEY: publicAnonFallback(),
};

function isPlaceholder(value) {
  return /^(changeme|change_me|placeholder|your_|example|todo|undefined|null)$/i.test(value.trim());
}

function validateUrl(name, value) {
  try {
    const parsed = new URL(value);
    if (parsed.protocol !== 'https:') {
      return `${name} must be an https:// URL`;
    }
    if (!parsed.hostname.endsWith('.supabase.co')) {
      return `${name} must point to a Supabase project URL`;
    }
    return undefined;
  } catch {
    return `${name} must be a valid URL`;
  }
}

function validateJwtLike(name, value) {
  const parts = value.split('.');
  if (parts.length !== 3 || parts.some((part) => part.length < 8)) {
    return `${name} must look like a Supabase anon JWT`;
  }
  return undefined;
}

function shellQuote(value) {
  return `'${value.replace(/'/g, `'"'"'`)}'`;
}

function parseWriteEnvPath() {
  const arg = process.argv.find((item) => item.startsWith('--write-env='));
  return arg ? arg.slice('--write-env='.length) : undefined;
}

const resolved = {};
const fallbackNames = [];

for (const name of required) {
  const provided = process.env[name];
  if (provided && !isPlaceholder(provided)) {
    resolved[name] = provided;
    continue;
  }

  resolved[name] = PUBLIC_BUILD_DEFAULTS[name];
  fallbackNames.push(name);
}

const errors = [];

const urlError = validateUrl('NEXT_PUBLIC_SUPABASE_URL', resolved.NEXT_PUBLIC_SUPABASE_URL);
if (urlError) errors.push(urlError);

const anonError = validateJwtLike(
  'NEXT_PUBLIC_SUPABASE_ANON_KEY',
  resolved.NEXT_PUBLIC_SUPABASE_ANON_KEY,
);
if (anonError) errors.push(anonError);

if (errors.length > 0) {
  console.error('Missing or invalid public Supabase build configuration:');
  for (const error of errors) {
    console.error(`- ${error}`);
  }
  console.error(
    'Set NEXT_PUBLIC_SUPABASE_URL and NEXT_PUBLIC_SUPABASE_ANON_KEY as Northflank build-time env/build args. Values are intentionally not echoed.',
  );
  process.exit(1);
}

const writeEnvPath = parseWriteEnvPath();
if (writeEnvPath) {
  const lines = [
    '# Generated by scripts/validate-next-public-build-env.mjs; values intentionally not logged.',
    ...required.map((name) => `export ${name}=${shellQuote(resolved[name])}`),
    '',
  ];
  writeFileSync(writeEnvPath, lines.join('\n'), { mode: 0o600 });
}

const suffix = fallbackNames.length > 0 ? ' (used public production fallback for missing build args)' : '';
console.info(`Public Supabase build env validated (values redacted)${suffix}.`);
