/**
 * netlify-quarantine-functions.mjs
 *
 * Moves backend-only Netlify functions out of netlify/functions before
 * Netlify deploys. Marketing Netlify should not carry cron jobs, migrations,
 * OCR, PDF export, job processors, or refund tracking — those belong on Railway.
 *
 * Usage:
 *   NETLIFY=true node scripts/netlify-quarantine-functions.mjs
 *   node scripts/netlify-quarantine-functions.mjs --restore
 *   node scripts/netlify-quarantine-functions.mjs --force   (local test)
 */

import { rename, mkdir, readdir } from 'fs/promises';
import { join, dirname } from 'path';
import { existsSync } from 'fs';

const RESTORE  = process.argv.includes('--restore');
const FORCE    = process.argv.includes('--force');
const IS_NETLIFY = process.env.NETLIFY === 'true' || FORCE;

if (!IS_NETLIFY && !RESTORE) {
  console.log('[fn-quarantine] Not on Netlify — skipping. Use --force to run locally.');
  process.exit(0);
}

const ROOT           = process.cwd();
const FUNCTIONS_DIR  = join(ROOT, 'netlify', 'functions');
const QUARANTINE_DIR = join(ROOT, '.netlify-quarantine', 'functions');

// Functions that must NOT deploy to Netlify marketing.
// ___netlify-server-handler is generated by @netlify/plugin-nextjs — never quarantine it.
const BACKEND_ONLY = new Set([
  // Cron jobs — Railway owns scheduled work
  'cron-social-media',
  'cron-onboarding-reminder',
  'cron-testing-reminders',
  'cron-orphan-check',
  'cron-compliance-expiration',
  'cron-testing-no-show',
  'cron-poll-mef-acks',
  'cron-testing-lead-followup',
  'cron-payout-deadline-alert',
  'cron-school-application-followup',
  // Backend jobs
  'onboarding-followup',
  'audit-export-cron',
  'job-processor',
  'run-migrations',
  // Document / media processing
  'export-pdf',
  'ocr-extract',
  'image-optimize',
  // SFC tax functions — SupersonicFastCash, not marketing
  'file-return',
  'refund-tracking',
]);

async function move(src, dest) {
  await mkdir(dirname(dest), { recursive: true });
  await rename(src, dest);
}

async function quarantine() {
  if (!existsSync(FUNCTIONS_DIR)) {
    console.log('[fn-quarantine] netlify/functions not found — skipping.');
    return;
  }

  const entries = await readdir(FUNCTIONS_DIR, { withFileTypes: true });
  let moved = 0;

  for (const entry of entries) {
    // Strip extension to get the function name (e.g. cron-social-media.ts → cron-social-media)
    const name = entry.name.replace(/\.(ts|js|mjs)$/, '');
    if (!BACKEND_ONLY.has(name)) continue;

    const src  = join(FUNCTIONS_DIR, entry.name);
    const dest = join(QUARANTINE_DIR, entry.name);
    await move(src, dest);
    console.log(`[fn-quarantine] quarantined: ${entry.name}`);
    moved++;
  }

  if (moved === 0) {
    console.log('[fn-quarantine] Nothing to quarantine.');
  } else {
    console.log(`[fn-quarantine] Quarantined ${moved} backend-only function(s).`);
  }

  // Report what remains
  const remaining = await readdir(FUNCTIONS_DIR, { withFileTypes: true });
  console.log(`[fn-quarantine] Remaining functions: ${remaining.map(e => e.name).join(', ') || '(none)'}`);
}

async function restore() {
  if (!existsSync(QUARANTINE_DIR)) {
    console.log('[fn-quarantine] Nothing to restore.');
    return;
  }

  const entries = await readdir(QUARANTINE_DIR, { withFileTypes: true });
  let restored = 0;

  for (const entry of entries) {
    const src  = join(QUARANTINE_DIR, entry.name);
    const dest = join(FUNCTIONS_DIR, entry.name);
    await mkdir(dirname(dest), { recursive: true });
    await rename(src, dest);
    restored++;
  }

  if (restored === 0) {
    console.log('[fn-quarantine] Nothing to restore.');
  } else {
    console.log(`[fn-quarantine] Restored ${restored} function(s).`);
  }
}

if (RESTORE) {
  await restore();
} else {
  await quarantine();
}
